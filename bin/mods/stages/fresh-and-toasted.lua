function onCreate()
    initLuaShader(shadname)
       
        makeLuaSprite('no-min', 'main/fresh-and-toasted/no-min', 0, -250)
        setGraphicSize('no-min', getProperty('no-min' .. '.width') * 1.75, getProperty('no-min'.height') * 1.75)
        setProperty('no-min.y', -1250)
        setGraphicSize('no-min', getProperty('no-min.width') * 2, getProperty('no-min.height') * 2)
        screenCenter('no-min', 'x')
        addLuaSprite('no-min', false)
        setSpriteShader('no-min', shadname)
        setShaderFloat('no-min', 'uWaveAmplitude', 0.1)
        setShaderFloat('no-min', 'uFrequency', 5)
        setShaderFloat('no-min', 'uSpeed', 2)

        makeLuaSprite('this_is_a_shuhbam-min', 'main/fresh-and-toasted/this_is_a_shuhbam-min', 0, -250)
        setGraphicSize('this_is_a_shuhbam-min', getProperty('no-min' .. '.width') * 1.75, getProperty('no-min'.height') * 1.75)
        setProperty('this_is_a_shuhbam-min.y', -1250)
        setGraphicSize('this_is_a_shuhbam-min', getProperty('no-min.width') * 2, getProperty('no-min.height') * 2)
        screenCenter('this_is_a_shuhbam-min', 'x')
        addLuaSprite('this_is_a_shuhbam-min', false)
        setSpriteShader('this_is_a_shuhbam-min', shadname)
        setShaderFloat('this_is_a_shuhbam-min', 'uWaveAmplitude', 0.1)
        setShaderFloat('this_is_a_shuhbam-min', 'uFrequency', 5)
        setShaderFloat('this_is_a_shuhbam-min', 'uSpeed', 2)
end